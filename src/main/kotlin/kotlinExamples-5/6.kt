package `kotlinExamples-5`

fun main(){
    //declared a variable with a value of 10
    var number = 10

    //while:
    //the body is executed while the condition is true

    while (number>=5){
        println("Number: $number")
        number--
    }
    while (number<=10){
        println("Number +: $number")
        number++
    }

}
